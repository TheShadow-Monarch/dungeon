function changecolor() {
    document.getElementById("myButton").style.backgroundColor = "black";
    document.getElementById("myButton").style.color = "white";
    document.getElementById("myButton").style.borderRadius = "5px";
    document.getElementById("myButton").style.border = "2px solid white";
    document.getElementById('myButton').style.backgroundColor="black";
  } 

function changecolorback() {
    document.getElementById("myButton").style.backgroundColor = "";
    document.getElementById("myButton").style.color = "";
    document.getElementById("myButton").style.borderRadius = "";
    document.getElementById("myButton").style.border = "";
  }

